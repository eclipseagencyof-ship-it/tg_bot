# --- Helper for inline keyboards ---
def make_kb(*rows):
    kb = InlineKeyboardMarkup(row_width=2)
    for row in rows:
        buttons = [InlineKeyboardButton(text=t, callback_data=c) for t, c in row]
        kb.row(*buttons)
    return kb


def input_file_safe(path: Path):
    if path.exists():
        return InputFile(str(path))
    return None


